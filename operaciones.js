// Declaración de las variables
let numero1 = 5;
let numero2 = 8;

// Operaciones aritméticas
let suma = numero1 + numero2;
let resta = numero1 - numero2;
let multiplicacion = numero1 * numero2;
let division = numero1 / numero2;
let modulo = numero1 % numero2;

// Comparaciones
let esMayor = numero1 > numero2;
let esMenor = numero1 < numero2;

// Mostrar los resultados en la terminal
console.log("Suma:", suma);
console.log("Resta:", resta);
console.log("Multiplicación:", multiplicacion);
console.log("División:", division);
console.log("Módulo:", modulo);
console.log("¿El primer número es mayor que el segundo?", esMayor);
console.log("¿El primer número es menor que el segundo?", esMenor);
